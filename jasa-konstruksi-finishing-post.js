    // 34. RETURN
    // ============================================================

    return {
        html: breadcrumbHtml,
        jsonLd,
        selectedLevels: uniqueLevels,
        currentPageType,
        entityType,
        version: '8.8.0 FINAL',
        maxLevel: 'NONE (nearest parent only)'
    };
