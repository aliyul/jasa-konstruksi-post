    function isLocation(text) {
        if (!text) return false;
        const lower = text.toLowerCase();
        for (const city of LOCATION_WHITELIST) {
            if (new RegExp(`\\b${city}\\b`, 'i').test(lower)) return true;
        }
        return false;
    }

